function cleanString(input) {
	var output = "";
	for (var i=0; i<input.length; i++) {
		if (input.charCodeAt(i) <= 255 || (input.charCodeAt(i) >=1024 && input.charCodeAt(i) <= 1327) ) {
		output += input.charAt(i);
		}
	}
	return output;
}

