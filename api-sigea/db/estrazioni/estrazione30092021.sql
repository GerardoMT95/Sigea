select
evento0_.evento_ID,
datigenera2_.titolo,
evento0_.tipologia_mibact,
evento0_.data_da_min,
evento0_.data_a_max,
locationse6_.provincia,locationse6_.comune,locationse6_.nome_location,
UPPER(dettagliou1_.nome ||' '|| dettagliou1_.cognome) NOME_ORGANIZZATORE,
(select contatto from sigec.t_SIGEA_CONTATTI contatti where contatti.evento_ID=evento0_.evento_ID and tipo like 'SITO' LIMIT 1) SITO,
(select contatto from sigec.t_SIGEA_CONTATTI contatti where contatti.evento_ID=evento0_.evento_ID and tipo like 'EMAIL' LIMIT 1) MAIL,
(select contatto from sigec.t_SIGEA_CONTATTI contatti where contatti.evento_ID=evento0_.evento_ID and tipo like 'TELEFONO' LIMIT 1) TELEFONO,
ticket.tipo_ticket, ticket.ticket_intero,ticket.nota, ticket.link_prenotazioni
from sigec.t_SIGEA_EVENTI evento0_ 
inner join sigec.t_SIGEA_DETTAGLIOUTENTE dettagliou1_ on evento0_.OWNER_ID=dettagliou1_.UTENTE_ID 
inner join sigec.t_SIGEA_DATIGENERALI datigenera2_ on evento0_.EVENTO_ID=datigenera2_.EVENTO_ID 
inner join sigec.t_SIGEA_STATI stato3_ on evento0_.STATO_ID=stato3_.STATO_ID 
inner join sigec.t_SIGEA_PUBBLICAZIONI PUBB on PUBB.EVENTO_ID=evento0_.EVENTO_ID and redazione_id like 'VIP'
left outer join sigec.t_SIGEA_ATTIVITA attivita4_ on  evento0_.ATTIVITA_ID=attivita4_.ATTIVITA_ID 
left outer join sigec.t_SIGEA_RICHIESTAATTIVITA richiestaa5_ on evento0_.RICHIESTA_ID=richiestaa5_.RICHIESTA_ID 
left outer join sigec.t_SIGEA_LOCATION locationse6_ on evento0_.EVENTO_ID=locationse6_.EVENTO_ID and (locationse6_.FG_PRINCIPALE=true) 
left outer join sigec.t_SIGEA_TICKETS ticket on ticket.EVENTO_ID= evento0_.evento_ID 
where (evento0_.STATO_ID is not null) 
and data_a_max>='2021-10-04'
and data_da_min<='2022-01-01'
and fg_pubblicato='true'
ORDER BY EVENTO_ID;



